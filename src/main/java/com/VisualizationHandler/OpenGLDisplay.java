    public void whiteNoiseLoop(){
        // This line is critical for LWJGL's interoperation with GLFW's
        // OpenGL context, or any context that is managed externally.
        // LWJGL detects the context that is current in the current thread,
        // creates the GLCapabilities instance and makes the OpenGL
        // bindings available for use.
        GL.createCapabilities();

        // Set the clear color
        glClearColor(1.0f, 0.0f, 0.0f, 0.0f);

        // Run the rendering loop until the user has attempted to close
        // the window or has pressed the ESCAPE key.
        while ( !glfwWindowShouldClose(window) ) {
            glClear(GL_COLOR_BUFFER_BIT | GL_DEPTH_BUFFER_BIT); // clear the framebuffer

            Random rand = new Random();

            int bufferSize = this.height * this.width * 3;
            float noiseData[] = new float[bufferSize];
            for (int i=0; i<bufferSize; i++){
                noiseData[i] = rand.nextFloat();
            }

            glPixelStorei(GL_UNPACK_ALIGNMENT, 1);

            glBegin(GL_QUADS);
            glTexCoord2f(0.0f, 0.0f); glVertex2f(-1.0f, -1.0f);
            glTexCoord2f(1.0f, 0.0f); glVertex2f( 1.0f, -1.0f);
            glTexCoord2f(1.0f, 1.0f); glVertex2f( 1.0f,  1.0f);
            glTexCoord2f(0.0f, 1.0f); glVertex2f(-1.0f,  1.0f);
            glEnd();

            // Upload the noise data as the pixel data
            glRasterPos2i(0, 0);
            glDrawPixels(this.width, this.height, GL_RGB, GL_FLOAT, noiseData);

            glfwSwapBuffers(window);
            glfwPollEvents();

            glfwSwapBuffers(window); // swap the color buffers

            // Poll for window events. The key callback above will only be
            // invoked during this call.
            glfwPollEvents();
        }
    }

